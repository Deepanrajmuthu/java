package switchcase;

public class switchcase {
	public static void main(String args[]){
		int tech = 2;
		switch(tech){
		case 1:
		{
		System.out.println("java");
		break;
		}
		case 2:{
		System.out.println("ES6");
		break;
		}
		default:{
		System.out.println("Not Listed..");
		}
		}
}
}


